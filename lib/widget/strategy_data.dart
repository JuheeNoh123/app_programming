  switch (resultType) {
    case '끊임없이\n성장하는 탐험가':
      return explorerStrategy;
    case '감성적으로\n세상을 해석하는\n크리에이터':
      return creatorStrategy;
    case '논리적으로\n계획하는 전략가':
      return strategistStrategy;
    default:
      return creatorStrategy;
  }
}
